package ca.hicham.test.forms;

public class Error {
long id;
long code;
String message;
public long getId() {
	return id;
}
public void setId(long id) {
	this.id = id;
}
public long getCode() {
	return code;
}
public void setCode(long code) {
	this.code = code;
}
public String getMessage() {
	return message;
}
public void setMessage(String message) {
	this.message = message;
}
}
